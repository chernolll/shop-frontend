-- =========================
-- 1. 生成 task_main（300条）
-- =========================
INSERT INTO task_main (
    product_listing_id,
    commission,
    video_num,
    deadline,
    type,
    budget,
    created_at,
    updated_at
)
SELECT
    pl.id,
    ROUND((random() * 100 + 20)::numeric, 2), -- 20~120佣金
    (random() * 10 + 1)::int, -- 1~10视频
    CURRENT_DATE + (random() * 30)::int, -- 未来30天
    (random() * 1)::int,
    (random() * 1)::int,
    now(),
    now()
FROM generate_series(1, 300) gs
JOIN LATERAL (
    SELECT id FROM product_listing ORDER BY random() LIMIT 1
) pl ON true;

-- =========================
-- 2. 生成 task_bd_relation（自动关联）
-- =========================
INSERT INTO task_bd_relation (
    task_id,
    bd_code,
    video_quantity,
    follow_entry_time,
    created_at,
    updated_at
)
SELECT
    t.id,
    bd.bd_code,
    (random() * 5 + 1)::int, -- 每个BD 1~5视频
    now(),
    now(),
    now()
FROM task_main t
JOIN LATERAL (
    SELECT bd_code
    FROM bd_person
    ORDER BY random()
    LIMIT (random() * 2 + 1)::int -- 每个任务1~3个BD
) bd ON true;

-- =========================
-- 3. 修正 video_num（等于BD分配总数，更真实）
-- =========================
UPDATE task_main t
SET video_num = sub.total
FROM (
    SELECT task_id, SUM(video_quantity) AS total
    FROM task_bd_relation
    GROUP BY task_id
) sub
WHERE t.id = sub.task_id;

-- =========================
-- 4. 随机生成部分“已过期任务”（更真实）
-- =========================
UPDATE task_main
SET deadline = CURRENT_DATE - (random() * 10)::int
WHERE random() < 0.3;

